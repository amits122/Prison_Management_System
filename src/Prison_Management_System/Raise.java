            String StaffID = (String)Selected_ID.getSelectedItem();
            int Index = Raise.getSelectedIndex();
            String query = "UPDATE STAFF SET Salary = Salary * 1.05 WHERE Staff_ID = '"+StaffID+"';";; 
            if(Index == 0)
                query = "UPDATE STAFF SET Salary = Salary * 1.05 WHERE Staff_ID = '"+StaffID+"';";
            else if(Index == 1)
                query = "UPDATE STAFF SET Salary = Salary * 1.10 WHERE Staff_ID = '"+StaffID+"';";
            else if(Index == 2)
                query = "UPDATE STAFF SET Salary = Salary * 1.15 WHERE Staff_ID = '"+StaffID+"';";
            else if(Index == 3)
                query = "UPDATE STAFF SET Salary = Salary * 1.20 WHERE Staff_ID = '"+StaffID+"';";
            Class.forName("java.sql.DriverManager");
            Connection con = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/pms", Login.dbUsername, Login.dbPassword);
            Statement stmt = (Statement)con.createStatement();
            stmt.executeUpdate(query);
            String line1 = "Raised Salary of" + StaffID; 
            String logger = "INSERT INTO LOG(User, Operation) VALUES('"+Login.username+"','"+line1+"');";
